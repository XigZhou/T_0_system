export function formatValue(key: string, value: unknown): string {
  if (value === null || value === undefined || Number.isNaN(value)) return "-";
  if (typeof value === "string") return valueLabels[value] || value;
  if (typeof value === "boolean") return value ? "\u662f" : "\u5426";
  if (typeof value !== "number") return String(value);
  if (percentKeys.has(key)) return `${(value * 100).toFixed(2)}%`;
  if (priceKeys.has(key)) return value.toLocaleString("zh-CN", { minimumFractionDigits: 2, maximumFractionDigits: 4 });
  if (moneyKeys.has(key)) return value.toLocaleString("zh-CN", { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  if (integerKeys.has(key)) return Math.round(value).toLocaleString("zh-CN");
  if (Math.abs(value) >= 1000) return value.toLocaleString("zh-CN", { maximumFractionDigits: 2 });
  return value.toLocaleString("zh-CN", { maximumFractionDigits: 6 });
}

export function formatHeader(key: string): string {
  return columnLabels[key] || key;
}

export function compactDate(value: string): string {
  if (!/^\d{8}$/.test(value)) return value;
  return `${value.slice(0, 4)}-${value.slice(4, 6)}-${value.slice(6)}`;
}
